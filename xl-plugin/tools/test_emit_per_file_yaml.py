def test_emitted_yaml_is_top_level_map_with_only_sections():
    with tempfile.TemporaryDirectory() as tmp:
        dst = Path(tmp) / "out.md.yaml"
        emit_per_file_yaml.emit(_payload_with_one_computation(dst))
        loaded = yaml.safe_load(dst.read_text())
        assert isinstance(loaded, dict)
        assert set(loaded.keys()) == {"sections"}


def test_emitted_yaml_has_preamble_with_source_path():
    with tempfile.TemporaryDirectory() as tmp:
        dst = Path(tmp) / "out.md.yaml"
        emit_per_file_yaml.emit(_payload_with_one_computation(dst))
        text = dst.read_text()
        assert "# Auto-generated by /extract-computations — do not edit manually" in text
        assert "# Source: input/policy_docs/a.md" in text


def test_round_trip_preserves_sections_structure():
    with tempfile.TemporaryDirectory() as tmp:
        dst = Path(tmp) / "out.md.yaml"
        payload = _payload_with_one_computation(dst)
        emit_per_file_yaml.emit(payload)
