def test_cli_reads_json_from_stdin_and_writes_yaml():
    with tempfile.TemporaryDirectory() as tmp:
        dst = Path(tmp) / "out.md.yaml"
        payload = _payload_with_one_variable(dst)
        result = subprocess.run(
            [sys.executable, os.path.join(os.path.dirname(__file__), "emit_per_file_yaml.py")],
            input=json.dumps(payload),
            capture_output=True,
            text=True,
            check=False,
        )
        assert result.returncode == 0, f"stderr: {result.stderr}"
        assert dst.exists()


def test_cli_exits_nonzero_on_validation_error():
    with tempfile.TemporaryDirectory() as tmp:
        dst = Path(tmp) / "out.md.yaml"
        payload = _payload_with_one_variable(dst)
        payload["sections"][0]["computations"][0]["variables"] = ["missing"]
        result = subprocess.run(
            [sys.executable, os.path.join(os.path.dirname(__file__), "emit_per_file_yaml.py")],
            input=json.dumps(payload),
            capture_output=True,
            text=True,
            check=False,
        )
        assert result.returncode != 0
        assert "missing" in result.stderr


