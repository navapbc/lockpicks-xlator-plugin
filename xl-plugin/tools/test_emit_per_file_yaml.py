def _minimal_payload(dst: Path, source_rel: str = "input/policy_docs/a.md") -> dict:
    return {
        "destination": str(dst),
        "source_rel": source_rel,
        "sections": [],
    }


def _payload_with_one_computation(dst: Path) -> dict:
    return {
        "destination": str(dst),
        "source_rel": "input/policy_docs/a.md",
        "sections": [
            {
                "heading": "# Income",
                "summary": "Income rules.",
                "tags": ["income"],
                "computations": [
                    {
                        "description": "Sum earned and unearned income.",
                        "expr_hint": "gross_income = earned_income + unearned_income",
                    },
                ],
            },
        ],
    }


