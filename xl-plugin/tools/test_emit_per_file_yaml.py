def test_destination_does_not_get_partial_content_on_validation_failure():
    with tempfile.TemporaryDirectory() as tmp:
        dst = Path(tmp) / "out.md.yaml"
        dst.write_text("PRIOR CONTENT\n")
        payload = _payload_with_one_computation(dst)
        payload["sections"][0]["computations"][0]["expr_hint"] = "no equals here"
        try:
            emit_per_file_yaml.emit(payload)
        except emit_per_file_yaml.ValidationError:
            pass
        assert dst.read_text() == "PRIOR CONTENT\n"


