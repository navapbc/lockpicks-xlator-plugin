def _minimal_payload(dst: Path, source_rel: str = "input/policy_docs/a.md") -> dict:
    return {
        "destination": str(dst),
        "source_rel": source_rel,
        "naming_manifest": {"variables": {}},
        "sections": [],
    }


def _payload_with_one_variable(dst: Path) -> dict:
    return {
        "destination": str(dst),
        "source_rel": "input/policy_docs/a.md",
        "naming_manifest": {
            "variables": {
                "gross_income": {
                    "policy_phrase": "gross monthly income",
                    "role_hint": "input",
                    "source_section": "§1.2",
                },
            },
        },
        "sections": [
            {
                "heading": "# Income",
                "summary": "Income rules.",
                "tags": ["income"],
                "computations": [
                    {
                        "description": "Sum of wages.",
                        "variables": ["gross_income"],
                    },
                ],
            },
        ],
    }


