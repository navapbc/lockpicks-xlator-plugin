def test_validates_every_section_variable_in_naming_manifest():
    with tempfile.TemporaryDirectory() as tmp:
        dst = Path(tmp) / "out.md.yaml"
        payload = _payload_with_one_variable(dst)
        payload["sections"][0]["computations"][0]["variables"] = [
            "gross_income",
            "missing_var",  # not in naming_manifest.variables
        ]
        try:
            emit_per_file_yaml.emit(payload)
        except emit_per_file_yaml.ValidationError as exc:
            assert "missing_var" in str(exc)
        else:
            raise AssertionError("expected ValidationError for missing_var")


def test_passes_when_all_section_variables_present_in_manifest():
    with tempfile.TemporaryDirectory() as tmp:
        dst = Path(tmp) / "out.md.yaml"
        emit_per_file_yaml.emit(_payload_with_one_variable(dst))
        assert dst.exists()


