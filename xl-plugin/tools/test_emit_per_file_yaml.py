def test_expr_hint_bare_expression_no_equals_is_rejected():
    """Legacy bare-expression form (no `=`) is rejected on emission."""
    with tempfile.TemporaryDirectory() as tmp:
        dst = Path(tmp) / "out.md.yaml"
        payload = _payload_with_one_computation(dst)
        payload["sections"][0]["computations"][0]["expr_hint"] = "earned_income + unearned_income"
        try:
            emit_per_file_yaml.emit(payload)
        except emit_per_file_yaml.ValidationError as exc:
            msg = str(exc)
            assert "expr_hint" in msg
            assert "=" in msg
            assert "output_name" in msg
        else:
            raise AssertionError("expected ValidationError for bare-expression expr_hint")


