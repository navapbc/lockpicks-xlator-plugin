def _make_domain(tmp: Path, name: str = "test_dom") -> Path:
    """Create a domain dir with the standard policy_facets/computations/ tree."""
    domain = tmp / name
    (domain / "policy_facets" / "computations").mkdir(parents=True)
    (domain / "input" / "policy_docs").mkdir(parents=True)
    (domain / "specs").mkdir(parents=True)
    return domain


def _write_per_file(
    domain: Path,
    rel: str,
    naming_manifest: dict | None,
    sections: list,
) -> Path:
    """Write a per-file map-shape YAML at policy_facets/computations/<rel>.yaml.

    `rel` is the source rel like "eligibility.md"; destination is
    `policy_facets/computations/<rel>.yaml` (suffix matches the per-file convention).
    Pass `naming_manifest=None` to omit the field entirely (partial-shape file).

    Auto-migration to the new schema: each variable entry that uses the legacy
    `source_section:` field gets the field renamed to `section:`, and entries
    that lack `source_doc:` get one auto-injected to match the file location
    (`input/policy_docs/<rel>.md`). Tests that need to exercise legacy or
    drift behavior bypass this helper and write raw YAML directly.
    """
    if naming_manifest is not None:
        variables = naming_manifest.get("variables") or {}
        if isinstance(variables, dict):
            full_path = f"input/policy_docs/{rel}"
            for entry in variables.values():
                if not isinstance(entry, dict):
                    continue
                if "source_section" in entry and "section" not in entry:
                    entry["section"] = entry.pop("source_section")
                if "source_doc" not in entry:
                    entry["source_doc"] = full_path
    dst = domain / "policy_facets" / "computations" / f"{rel}.yaml"
    dst.parent.mkdir(parents=True, exist_ok=True)
    body: dict = {}
    if naming_manifest is not None:
        body["naming_manifest"] = naming_manifest
    body["sections"] = sections
    with dst.open("w", encoding="utf-8") as f:
        f.write("# Auto-generated by /extract-computations — do not edit manually\n")
        f.write(f"# Source: input/policy_docs/{rel}\n\n")
        yaml.safe_dump(body, f, default_flow_style=False, allow_unicode=True, sort_keys=False)
    return dst


def _write_specs(domain: Path, payload: dict) -> Path:
    path = domain / "specs" / "naming-manifest.yaml"
    with path.open("w", encoding="utf-8") as f:
        yaml.safe_dump(payload, f, default_flow_style=False, allow_unicode=True, sort_keys=False)
    return path


def _read_defaults(domain: Path) -> dict:
    path = domain / "policy_facets" / "naming-defaults.yaml"
    with path.open(encoding="utf-8") as f:
        return yaml.safe_load(f) or {}


def _read_per_file(domain: Path, rel: str) -> dict:
    path = domain / "policy_facets" / "computations" / f"{rel}.yaml"
    with path.open(encoding="utf-8") as f:
        return yaml.safe_load(f) or {}


