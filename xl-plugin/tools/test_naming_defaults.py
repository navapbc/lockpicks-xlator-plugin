def test_legacy_source_section_emits_warning():
    """Per-file file with legacy `source_section:` (no `section:`, no
    `source_doc:`) produces an actionable warning in summary['errors'].
    The build still completes; output entry has no section field."""
    with tempfile.TemporaryDirectory() as tmp:
        domain = _make_domain(Path(tmp))
        # Bypass _write_per_file's auto-migration; write raw legacy YAML.
        dst = domain / "policy_facets" / "computations" / "legacy.md.yaml"
        dst.parent.mkdir(parents=True, exist_ok=True)
        dst.write_text(
            "# Auto-generated by /extract-computations — do not edit manually\n"
            "# Source: input/policy_docs/legacy.md\n\n"
            "naming_manifest:\n"
            "  variables:\n"
            "    gross_income:\n"
            "      policy_phrase: \"gross monthly income\"\n"
            "      source_section: \"§1.2\"\n"
            "sections: []\n",
            encoding="utf-8",
        )
        summary = naming_defaults.cmd_build(domain)
        legacy_warnings = [
            e for e in summary["errors"] if "legacy 'source_section:'" in e
        ]
        assert len(legacy_warnings) == 1
        assert "gross_income" in legacy_warnings[0]
        assert "delete and re-run /extract-computations" in legacy_warnings[0]
        # Build still completes; output entry has no section.
        defaults = _read_defaults(domain)
        entry = defaults["variables"]["gross_income"]
        assert "section" not in entry
        # source_doc falls back to file location.
        assert entry["source_doc"] == "input/policy_docs/legacy.md"


