def test_stability_heuristic_prefers_prior_canonical_on_tie():
    """Pure tie (frequency, length all equal) → prefer prior canonical when present."""
    with tempfile.TemporaryDirectory() as tmp:
        domain = _make_domain(Path(tmp))
        # Two equal-length, equal-frequency names: "alpha_n" (7) and "beta_nn" (7).
        # Alphabetical tie-break would pick alpha_n; prior says beta_nn.
        prior_path = domain / "policy_facets" / "naming-defaults.yaml"
        prior_path.parent.mkdir(parents=True, exist_ok=True)
        prior_path.write_text(
            "# Auto-generated by xlator naming-defaults --build — do not edit manually\n"
            "variables:\n"
            "  beta_nn:\n"
            "    policy_phrase: \"the concept\"\n"
            "    sources:\n"
            "      - file: a.md\n"
            "        section: '§1'\n",
            encoding="utf-8",
        )
        _write_per_file(
            domain, "a.md",
            naming_manifest={"variables": {"alpha_n": {
                "policy_phrase": "the concept", "source_section": "§1",
            }}},
            sections=[{"heading": "# h", "summary": "", "tags": [],
                       "computations": [{"description": "", "variables": ["alpha_n"]}]}],
        )
        _write_per_file(
            domain, "b.md",
            naming_manifest={"variables": {"beta_nn": {
                "policy_phrase": "the concept", "source_section": "§2",
            }}},
            sections=[{"heading": "# h", "summary": "", "tags": [],
                       "computations": [{"description": "", "variables": ["beta_nn"]}]}],
        )
        naming_defaults.cmd_build(domain)
        defaults = _read_defaults(domain)
        # Stability heuristic kept beta_nn even though alpha_n is alphabetically first.
        assert "beta_nn" in defaults["variables"]
        assert defaults["variables"]["beta_nn"]["synonyms"] == ["alpha_n"]


