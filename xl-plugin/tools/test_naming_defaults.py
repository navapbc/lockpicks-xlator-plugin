def test_ae1_two_files_disagree_shortest_wins():
    """AE1: file A has gross_income, file B has monthly_gross — same phrase.
    canonical = gross_income (tied freq=1, shortest wins); B's variables: rewritten."""
    with tempfile.TemporaryDirectory() as tmp:
        domain = _make_domain(Path(tmp))

        _write_per_file(
            domain, "eligibility.md",
            naming_manifest={
                "variables": {
                    "gross_income": {
                        "policy_phrase": "gross monthly income",
                        "source_section": "§1.2",
                    },
                },
            },
            sections=[{
                "heading": "# Income",
                "summary": "...",
                "tags": ["income"],
                "computations": [{"description": "...", "variables": ["gross_income"]}],
            }],
        )
        _write_per_file(
            domain, "income.md",
            naming_manifest={
                "variables": {
                    "monthly_gross": {
                        "policy_phrase": "gross monthly income",
                        "source_section": "§2.1",
                    },
                },
            },
            sections=[{
                "heading": "# Earned Income",
                "summary": "...",
                "tags": ["income"],
                "computations": [{"description": "...", "variables": ["monthly_gross"]}],
            }],
        )

        summary = naming_defaults.cmd_build(domain)

        defaults = _read_defaults(domain)
        assert "gross_income" in defaults["variables"]
        assert defaults["variables"]["gross_income"]["synonyms"] == ["monthly_gross"]
        assert defaults["variables"]["gross_income"]["policy_phrase"] == "gross monthly income"
        # File B's variables list rewritten to canonical.
        b = _read_per_file(domain, "income.md")
        assert b["sections"][0]["computations"][0]["variables"] == ["gross_income"]
        # File A unchanged on canonical name.
        a = _read_per_file(domain, "eligibility.md")
        assert a["sections"][0]["computations"][0]["variables"] == ["gross_income"]
        assert summary["merged"] == 1
        assert summary["synonyms_collapsed"] == 1
        assert summary["files_rewritten"] == 1


