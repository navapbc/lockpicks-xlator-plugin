def test_synonym_row_source_doc_from_entry_not_path():
    """When two observations of the same canonical exist, each synonym row's
    source_doc comes from its per-file entry (verbatim) — even when the entry
    declares a non-default source_doc that disagrees with file location."""
    with tempfile.TemporaryDirectory() as tmp:
        domain = _make_domain(Path(tmp))
        # Canonical observation at a.md, normal source_doc.
        _write_per_file(
            domain, "a.md",
            naming_manifest={"variables": {"gi": {
                "policy_phrase": "gross income", "section": "§1",
            }}},
            sections=[{"heading": "# h", "summary": "", "tags": [],
                       "computations": [{"description": "", "variables": ["gi"]}]}],
        )
        # Synonym observation at b.md but with source_doc declared as c.md.
        # Drift warning expected; synonym row carries c.md verbatim.
        dst_b = domain / "policy_facets" / "computations" / "b.md.yaml"
        dst_b.parent.mkdir(parents=True, exist_ok=True)
        dst_b.write_text(
            "# Auto-generated by /extract-computations — do not edit manually\n"
            "# Source: input/policy_docs/b.md\n\n"
            "naming_manifest:\n"
            "  variables:\n"
            "    gi_alt:\n"
            "      policy_phrase: \"gross income\"\n"
            "      source_doc: \"input/policy_docs/c.md\"\n"
            "      section: \"§2\"\n"
            "sections:\n"
            "  - heading: '# h'\n"
            "    summary: ''\n"
            "    tags: []\n"
            "    computations:\n"
            "      - description: ''\n"
            "        variables: [gi_alt]\n",
            encoding="utf-8",
        )
        summary = naming_defaults.cmd_build(domain)
        defaults = _read_defaults(domain)
        entry = defaults["variables"]["gi"]
        # Top-level source_doc from canonical observation (a.md, no drift).
        assert entry["source_doc"] == "input/policy_docs/a.md"
        # Synonym row source_doc verbatim from b.md's entry (which says c.md).
        assert entry["synonyms"] == [{
            "name": "gi_alt",
            "source_doc": "input/policy_docs/c.md",
            "section": "§2",
        }]
        # Drift warning emitted.
        drift_warnings = [
            e for e in summary["errors"]
            if "disagrees with file location" in e
        ]
        assert len(drift_warnings) == 1


