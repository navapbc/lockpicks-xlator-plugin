def test_atomic_write_no_partial_file_on_failure(monkeypatch=None):
    """Mock os.replace to fail; verify no partial file at destination, tmp cleaned up.

    Uses a manual monkeypatch since pytest fixtures are not used in this file.
    """
    with tempfile.TemporaryDirectory() as tmp:
        domain = _make_domain(Path(tmp))
        _write_per_file(
            domain, "a.md",
            naming_manifest={"variables": {"gross_income": {
                "policy_phrase": "gross monthly income", "source_section": "§1",
            }}},
            sections=[{"heading": "# h", "summary": "", "tags": [],
                       "computations": [{"description": "", "variables": ["gross_income"]}]}],
        )

        defaults_path = domain / "policy_facets" / "naming-defaults.yaml"
        # Pre-write a known prior content so we can check it is preserved on failure.
        defaults_path.write_text("prior: content\n", encoding="utf-8")

        original_replace = os.replace
        calls: list = []

        def boom_replace(src, dst):
            calls.append((src, dst))
            # Only fail on the naming-defaults.yaml write, not per-file rewrites.
            if str(dst).endswith("naming-defaults.yaml"):
                raise OSError("simulated rename failure")
            return original_replace(src, dst)

        os.replace = boom_replace
        try:
            try:
                naming_defaults.cmd_build(domain)
            except OSError:
                pass
        finally:
            os.replace = original_replace

        # Prior content preserved (or removed cleanly) — never partial.
        assert defaults_path.read_text(encoding="utf-8") == "prior: content\n"
        # Tmp file should not be left behind.
        tmp_files = list((domain / "policy_facets").glob("naming-defaults.yaml.tmp"))
        assert tmp_files == []


